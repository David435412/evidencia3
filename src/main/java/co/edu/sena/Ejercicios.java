package co.edu.sena;

public class Ejercicios {

    public double areaTriangulo(double d1, double d2){
        return (d1 * d1) / 2;
    }
    public double sumarNumeros(double d1, double d2){
        return (d1 + d2);
    }
    public double numeroCuadrado(double d1){
        return (d1 * d1);
    }
}
    